/* A literal is a Literal representation of a value in JavaScript--a fixed value-- not a variable-- they are literally provided in the code

a variable is not a literal, the information assigned to the variable is the literal
    ex:
(keyword)           (value/literal)
    let placeholder = 'text';
        (variable)
 */

//  STRING LITERAL
let city = 'Indianapolis';

// NUMERIC LITERALS
let age = 35;

// BOOLEAN LITERAL
let isOn = true;

// OBJECT LITERAL
let movie = {
    title: 'Elf',
    rating: 'PG',
    release: 'November 7th, 2003'
};

// ARRAY LITERAL
let daysOfWeek = ['Sunday,', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'];


